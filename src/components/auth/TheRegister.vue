  position: absolute;
  z-index: 1;
  margin: 0;
  position: absolute;
  top: 50%;
  left: 55%;
  -ms-transform: translate(-50%, -50%);
  transform: translate(-50%, -50%);
}

.password-container {
  position: relative;
  .eye {
    position: absolute;
    right: 15px;
    bottom: 20px;
    transition: all ease-in 200ms;
  }
  input:focus-within + .eye {
    bottom: 25px;
  }
}
