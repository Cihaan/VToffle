<script lang="ts" setup>
import { reactive } from "vue";
import axios from "axios";
import TheLoading from "../utils/TheLoading.vue";
import TheNotifBox from "../utils/TheNotifBox.vue";

const data = reactive({
  name: "",
  email: "",
  password: "",
  state: "",
  to: "login",
  title: "",
  msg: "",
});

function showPassword(event: Event): void {
  let element = event.target as HTMLElement;

  if (element.getAttribute("alt") === "show password") {
    element.setAttribute("src", "/src/assets/logos/eye_closed.svg");
    element.setAttribute("alt", "hide password");
    element.previousElementSibling?.setAttribute("type", "text");
  } else {
    element.setAttribute("src", "/src/assets/logos/eye_open.svg");
    element.setAttribute("alt", "show password");
    element.previousElementSibling?.setAttribute("type", "password");
  }
}

async function handleSubmit(): Promise<void> {
  let user = {
    username: data.name,
    email: data.email,
    password: data.password,
  };

  data.state = "sending";
  let res = await axios
    .post("http://localhost:5000/auth/register", user)
    .then((rep) => {
      data.state = "sent";
      data.title = "Congratulations !";
      data.msg = "Your account has been created successfully !";
      data.to = "login";
    })
    .catch((err) => {
      data.state = "sent";
      data.title = "Oops !";
      data.msg = "Your credentials may have already been used !";
      data.to = "";
    });
}
</script>

<template>
  <div class="container">
    <router-link to="/landing">
      <i class="fas fa-chevron-left title"></i>
    </router-link>
    <h2 class="spaced-title">Create Account</h2>
    <div>
      <label for="name">Name</label>
      <input v-model="data.name" id="name" name="name" type="text" class="text-field" />
      <label for="email">Email</label>
      <input
        v-model="data.email"
        id="email"
        name="email"
        type="email"
        class="text-field"
      />
      <div class="center" v-if="data.state === 'sending'">
        <TheLoading />
      </div>
      <TheNotifBox
        v-if="data.state === 'sent'"
        :title="data.title"
        :msg="data.msg"
        :to="data.to"
        class="center"
      />
      <label for="password">Password</label>

      <div class="password-container">
        <input
          v-model="data.password"
          id="password"
          name="password"
          type="password"
          class="text-field"
        />

        <img
          class="eye"
          @click="showPassword"
          src="../../assets/logos/eye_open.svg"
          alt="show password"
        />
      </div>
    </div>
    <div class="buttons">
      <router-link to="/login">
        <input type="button" class="btn btn-primary" value="Log in" />
      </router-link>
      <hr class="hrr" />
      <input
        type="submit"
        @click="handleSubmit"
        class="btn btn-secondary"
        value="Sign up"
      />
    </div>
  </div>
</template>

<style lang="scss" scoped>
